// Windows Virtual-Key codes. Centralized so the scenario DSL never sees
// raw 0x57 numbers and "Unknown key" warnings disappear.
//
// Reference: https://learn.microsoft.com/en-us/windows/win32/inputdev/virtual-key-codes

export const Keys = {
  // Letters
  a: 0x41, b: 0x42, c: 0x43, d: 0x44, e: 0x45, f: 0x46, g: 0x47, h: 0x48,
  i: 0x49, j: 0x4A, k: 0x4B, l: 0x4C, m: 0x4D, n: 0x4E, o: 0x4F, p: 0x50,
  q: 0x51, r: 0x52, s: 0x53, t: 0x54, u: 0x55, v: 0x56, w: 0x57, x: 0x58,
  y: 0x59, z: 0x5A,

  // Digits (top row)
  '0': 0x30, '1': 0x31, '2': 0x32, '3': 0x33, '4': 0x34,
  '5': 0x35, '6': 0x36, '7': 0x37, '8': 0x38, '9': 0x39,

  // Modifiers
  shift: 0x10, ctrl: 0x11, alt: 0x12,
  lshift: 0xA0, rshift: 0xA1,
  lctrl: 0xA2, rctrl: 0xA3,
  lalt: 0xA4, ralt: 0xA5,
  lwin: 0x5B, rwin: 0x5C,

  // Whitespace / navigation
  space: 0x20, enter: 0x0D, tab: 0x09, escape: 0x1B, backspace: 0x08,
  delete: 0x2E, insert: 0x2D,
  home: 0x24, end: 0x23, pageup: 0x21, pagedown: 0x22,
  up: 0x26, down: 0x28, left: 0x25, right: 0x27,

  // Function row
  f1: 0x70, f2: 0x71, f3: 0x72, f4: 0x73, f5: 0x74, f6: 0x75,
  f7: 0x76, f8: 0x77, f9: 0x78, f10: 0x79, f11: 0x7A, f12: 0x7B,

  // Numpad
  num0: 0x60, num1: 0x61, num2: 0x62, num3: 0x63, num4: 0x64,
  num5: 0x65, num6: 0x66, num7: 0x67, num8: 0x68, num9: 0x69,
  numadd: 0x6B, numsub: 0x6D, nummul: 0x6A, numdiv: 0x6F, numdot: 0x6E,

  // OEM (US layout)
  ';': 0xBA, '=': 0xBB, ',': 0xBC, '-': 0xBD, '.': 0xBE, '/': 0xBF,
  '`': 0xC0, '[': 0xDB, '\\': 0xDC, ']': 0xDD, "'": 0xDE,
} as const;

export type KeyName = keyof typeof Keys;
export type VirtualKey = number;

export function resolveKey(key: KeyName | VirtualKey): number {
  if (typeof key === 'number') return key;
  const vk = Keys[key];
  if (vk === undefined) {
    throw new Error(
      `Unknown key '${String(key)}'. Valid names: ${Object.keys(Keys).join(', ')}`
    );
  }
  return vk;
}
